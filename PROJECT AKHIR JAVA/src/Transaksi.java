    private void idtransKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idtransKeyPressed
        // TODO add your handling code here:
        ID_trans = idtrans.getText();
        int A = evt.getKeyCode();
        if (A == 10) {
            try {
                Sql = "SELECT * FROM transaksi WHERE id_transaksi='" + ID_trans + "'";
                st = Con.createStatement();
                Rs = st.executeQuery(Sql);
                while (Rs.next()) {
                    idpeg.setText(Rs.getString(2));
                    kon_pem.setText(Rs.getString(3));
                    b_akhir.setText(Rs.getString(4));
                    b_akhir1.setText(Rs.getString(5));
                    b_akhir2.setText(Rs.getString(6));
                    save.enable(false);
                    edit.enable(true);
                    hapus.enable(true);
                }
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, "Data Not Found\n" + e.getMessage());
                idpeg.requestFocus();
            }
        }
    }//GEN-LAST:event_idtransKeyPressed

