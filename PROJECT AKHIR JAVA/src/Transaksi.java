    public void autoidtrans() {
        try {

            String sql = "select right(id_transaksi,2) as no_akhir from transaksi order by id_transaksi asc";
            st = Con.createStatement();
            Rs = st.executeQuery(sql);

            if (Rs.first() == false) {
                idtrans.setText("T0001");
            } else {
                Rs.last();
                int no = Rs.getInt(1) + 1;
                String cno = String.valueOf(no);
                int pjg_cno = cno.length();
                for (int i = 0; i < 2 - pjg_cno; i++) {
                    cno = "00" + cno;
                }
                idtrans.setText("T0" + cno);
            }
        } catch (Exception e) {
            System.err.println(e);
        }

    }

    private void TampilTransaksi(String Sql) {
        DefaultTableModel grid = new DefaultTableModel();
        grid.addColumn("ID Transaksi");
        grid.addColumn("ID Pegawai");
        grid.addColumn("Kontak Pembeli");
        grid.addColumn("Tanggal Masuk");
        grid.addColumn("Tanggal Keluar");
        grid.addColumn("Bayar Akhir");
        try {
            String sql = "SELECT * FROM transaksi;";
            st = Con.prepareStatement(sql);
            Rs = st.executeQuery(sql);
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            String[] data = new String[6];
            int i = 1;

            while (Rs.next()) {
                data[0] = Rs.getString("id_transaksi");
                data[1] = Rs.getString("kontak_pembeli");
                data[2] = Rs.getString("tanggal_masuk");
                data[3] = Rs.getString("tanggal_keluar");
                data[4] = Rs.getString("id_pegawai");
                data[5] = Rs.getString("bayar_akhir");
                dtm.addRow(data);
                i++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

